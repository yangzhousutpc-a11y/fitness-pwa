export function getExerciseById(id: string): Exercise | undefined {
  return exercises.find((exercise) => exercise.id === id);
}

export function searchExercises(query: string): Exercise[] {
  const normalizedQuery = query.trim().toLowerCase();

  if (!normalizedQuery) {
    return exercises;
  }

  return exercises.filter((exercise) => {
    const searchable = [
      exercise.name,
      exercise.equipment,
      ...exercise.muscleGroups,
      ...exercise.cues,
      ...exercise.commonMistakes,
    ]
      .join(' ')
      .toLowerCase();

    return searchable.includes(normalizedQuery);
  });
}

export type ExerciseFilter = '全部' | MuscleGroup | '手臂';

export function filterExercises(query: string, filter: ExerciseFilter): Exercise[] {
  const results = searchExercises(query);

  if (filter === '全部') {
    return results;
  }

  if (filter === '手臂') {
    return results.filter(
      (exercise) => exercise.muscleGroups.includes('二头') || exercise.muscleGroups.includes('三头'),
    );
  }

  return results.filter((exercise) => exercise.muscleGroups.includes(filter));
}
